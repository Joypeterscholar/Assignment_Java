package snacks1;

public class User {
    private String name;
    private int age;
    private String email;
    private String address;
    private String password;
    private String phone;
    public User(String name, int age, String email, String address, String password, String phone){
        this.name = name;
        this.age = age;
        this.email = email;
        this.address = address;
        this.password = password;
        this.phone = phone;
    }

}
